package Pojo;

public class Ptag {
        public int id;
        public String name;

        public Ptag(int id, String name) {
                this.id = id;
                this.name = name;
        }

        public Ptag() {
        }

        public int getId() {
                return id;
        }

        public void setId(int id) {
                this.id = id;
        }

        public String getName() {
                return name;
        }

        public void setName(String name) {
                this.name = name;
        }

}
